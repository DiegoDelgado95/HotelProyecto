    //Ocultar_Columna me oculta la columna de ID de Persona
    void ocultar_columna(){
        tableCliente.getColumnModel().getColumn(0).setMaxWidth(0);
        tableCliente.getColumnModel().getColumn(0).setMinWidth(0);
        tableCliente.getColumnModel().getColumn(0).setPreferredWidth(0);
        
    }
    
    //Funcion para llenar la tabla con persona
    void mostrar (String buscar){
        try {
            DefaultTableModel modelo;
            dbPersona func = new dbPersona();
            modelo = func.mostrar(buscar);
            
            tableCliente.setModel(modelo);
            ocultar_columna();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    

