    private void tableClienteMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableClienteMousePressed
        if(evt.getClickCount() == 2 ){
            int fila = tableCliente.getSelectedRow();
            String cod;
            String valor;
            
            cod = tableCliente.getValueAt(fila,0).toString();
            valor = tableCliente.getValueAt(fila,1).toString() + " " + tableCliente.getValueAt(fila,2).toString();
            
            ABM_Ocupacion.txtIDCliente.setText(cod);
            ABM_Ocupacion.txtCliente.setText(valor);
            this.dispose();
            
        }
    }//GEN-LAST:event_tableClienteMousePressed

